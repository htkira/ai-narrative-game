export const STORY_SKELETON = `
# 白家村·第一世第一幕：空屋醒来 — 剧情骨架

## 世界背景
玩家化身游方郎中，行至白虎岭下白家村。村中突发"白骨症"——患者会精神错乱，最终化作白骨死去。村民将灾祸归因于山中妖邪"白姑"，视其为食人妖魔。实际上灾祸另有根源，白姑并非纯恶之物，但完整真相不在本幕揭示。

## 真实事件链（必须严格遵守）
1. 主角此前被这户村民请来诊病
2. 患者在诊疗过程中突然发病，攻击主角
3. 主角被袭昏倒
4. 主角昏迷期间，白姑闯入屋中，与发病村民发生斗争
5. 发病村民死去
6. 白姑收集了死者遗留的骸骨与部分相关物品后离开
7. 主角在空屋中醒来，发现屋内一片狼藉

## 角色定义
- **主角**：游方郎中，非侦探，擅长病理观察、药理判断、异常征候辨识。对某些异常有超出常人的感知。
- **辩论NPC**：代表村民集体偏见的对手。身份由AI动态生成（可以是村长、族老、猎户头子、药婆、庙祝等任何合理的山村权威人物）。核心特征：惊慌失措、先入为主、不擅严密推理。会嘴硬、补锅、后撤。
- **白姑**：既非纯善也非纯恶。来过现场，但动机复杂。本幕不揭示其完整立场。

## 物品分类规则
所有可交互物品分为四类：
1. **atmosphere（纯氛围物）**：建立空间真实感、生活感与压抑气氛，不推动主线。
2. **false_clue（伪线索物）**：看起来可疑但只提供局部/误导信息。帮助NPC形成看似合理的错误叙事。
3. **true_clue（真线索物）**：帮助玩家拼出真相的关键物证。大部分需借助残缺念珠才显露价值。
4. **special（残缺念珠）**：每局必出现的特殊物件。使主角触碰关键物时能看见"最后一次强烈执念的一念残影"。

## 残缺念珠

- 残缺念珠是第一幕固定出现的特殊物件，也是主角开始真正“看见异样”的转折点。
- 它带有朦胧的佛教与轮回色彩，它不是一整串完整念珠，而是一段残缺、不全、带有旧感和异样气息的珠串残片。它看起来像曾经属于某种更完整之物，如今只余少数珠子与断裂痕迹。
- 残缺念珠的意义主要有三层：
    - 表层：它是一件偶然被玩家找到的神秘物件。
    - 中层：它能帮助主角看到物品上残留的一念，成为探索机制的升级节点。
    - 深层：它似乎并非普通失物，而像是会反复进入主角命运中的东西，带有若有若无的轮回跟随意味。

## 残缺念珠规则
- 每次生成的场景中都必须有残缺念珠，它的出现位置不固定，允许每次游玩都不同。
- 残缺念珠的能力：触碰物品时看见其"最后一次留下强烈执念的一念残影"
- 残影特点：主观、片段极短、可能受情绪扭曲、只够形成印象
- 本幕中带有朦胧的佛教与轮回色彩，但不完整解释

## 线索类型
- **pathology**：病理观察（异常发病、体征）
- **pharmacology**：药理判断（药渣、药方、异常配伍）
- **conflict_trace**：冲突痕迹（打斗、挣扎、破坏）
- **bead_memory**：念珠残念（通过残缺念珠获得的主观残影）
- **misc**：其他有助推理的杂项线索

## 主角视角要求
主角对物品和现场的描述应体现"郎中如何看"：
- 偏向病理、药理、身体损伤的专业观察
- 区分"正常死因"与"异常死因"
- 不是单纯"看见了什么"，而是专业判断

## 本幕叙事约束
**不得揭示**：白骨症真正源头、山神/龙脉/伪道士完整因果、白姑完整立场和动机、主角轮回身份、残缺念珠完整来历。
**可以轻微显露**：主角异常感知、残缺念珠非普通之物、宿命与前缘的意味。

## NPC辩论观点要求
- 3条主要观点，每条建立在一个可被物证否定的错误前提上
- 观点的本质是NPC基于偏见对现象做出的错误归因/推断，而非对物证内容的复述
- 观点应对立以下真相线：A（死者先发病）、B（双向对抗非单方面行凶）、D（收集搬运非食人）、E（白姑行为有非恶意特征）
- 至少1条观点的论据来自伪线索
- NPC态度阶段：assertive → defensive → shaken → retreating → confessing
- NPC不掌握完整真相，后撤过程应自然可信

## 玩家在探索结束后应能形成的判断
- 死者死前状态异常，疑似先发病
- 屋内曾发生过外来者与病人的激烈对抗
- 白姑来过现场
- 白姑带走了骨与部分物品
- 白姑的行为未必符合"妖怪食人"的简单叙事
`.trim();

